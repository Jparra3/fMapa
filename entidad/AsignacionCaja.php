<?php
namespace entidad;
class AsignacionCaja {
    private $idAsignacionCaja;
    private $idCajero;
    private $idCaja;
    private $estado;
    private $fechaHoraInicial;
    private $fechaHoraFinal;
    
    function getIdAsignacionCaja() {
        return $this->idAsignacionCaja;
    }

    function getIdCajero() {
        return $this->idCajero;
    }

    function getIdCaja() {
        return $this->idCaja;
    }

    function getEstado() {
        return $this->estado;
    }

    function getFechaHoraInicial() {
        return $this->fechaHoraInicial;
    }

    function getFechaHoraFinal() {
        return $this->fechaHoraFinal;
    }

    function setIdAsignacionCaja($idAsignacionCaja) {
        $this->idAsignacionCaja = $idAsignacionCaja;
    }

    function setIdCajero($idCajero) {
        $this->idCajero = $idCajero;
    }

    function setIdCaja($idCaja) {
        $this->idCaja = $idCaja;
    }

    function setEstado($estado) {
        $this->estado = $estado;
    }

    function setFechaHoraInicial($fechaHoraInicial) {
        $this->fechaHoraInicial = $fechaHoraInicial;
    }

    function setFechaHoraFinal($fechaHoraFinal) {
        $this->fechaHoraFinal = $fechaHoraFinal;
    }


}
