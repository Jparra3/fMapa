<?php

namespace entidad;
class Factura {
    private $idFactura;
    private $clienteServicio;
    private $fechaInicial;
    private $fechaFinal;
    private $idEstadoFactura;
    private $valor;
    private $idUsuarioCreacion;
    private $idUsuarioModificacion;
    private $fechaCreacion;
    private $fechaModificacion;
    
    function getIdFactura() {
        return $this->idFactura;
    }

    function getClienteServicio() {
        return $this->clienteServicio;
    }

    function getFechaInicial() {
        return $this->fechaInicial;
    }

    function getFechaFinal() {
        return $this->fechaFinal;
    }

    function getIdEstadoFactura() {
        return $this->idEstadoFactura;
    }

    function getValor() {
        return $this->valor;
    }

    function getIdUsuarioCreacion() {
        return $this->idUsuarioCreacion;
    }

    function getIdUsuarioModificacion() {
        return $this->idUsuarioModificacion;
    }

    function getFechaCreacion() {
        return $this->fechaCreacion;
    }

    function getFechaModificacion() {
        return $this->fechaModificacion;
    }

    function setIdFactura($idFactura) {
        $this->idFactura = $idFactura;
    }

    function setClienteServicio($clienteServicio) {
        $this->clienteServicio = $clienteServicio;
    }

    function setFechaInicial($fechaInicial) {
        $this->fechaInicial = $fechaInicial;
    }

    function setFechaFinal($fechaFinal) {
        $this->fechaFinal = $fechaFinal;
    }

    function setIdEstadoFactura($idEstadoFactura) {
        $this->idEstadoFactura = $idEstadoFactura;
    }

    function setValor($valor) {
        $this->valor = $valor;
    }

    function setIdUsuarioCreacion($idUsuarioCreacion) {
        $this->idUsuarioCreacion = $idUsuarioCreacion;
    }

    function setIdUsuarioModificacion($idUsuarioModificacion) {
        $this->idUsuarioModificacion = $idUsuarioModificacion;
    }

    function setFechaCreacion($fechaCreacion) {
        $this->fechaCreacion = $fechaCreacion;
    }

    function setFechaModificacion($fechaModificacion) {
        $this->fechaModificacion = $fechaModificacion;
    }
}
