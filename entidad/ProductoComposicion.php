<?php
namespace entidad;
class ProductoComposicion {
    private $idProductoComposicion;
    private $idProductoCompuesto;
    private $cantidad;
    private $idProductoCompone;
    private $estado;
    private $idUsuarioCreacion;
    private $idUsuarioModificacion;
    private $fechaCreacion;
    private $fechaModificacion;
    
    function getIdProductoComposicion() {
        return $this->idProductoComposicion;
    }

    function getIdProductoCompuesto() {
        return $this->idProductoCompuesto;
    }

    function getCantidad() {
        return $this->cantidad;
    }

    function getIdProductoCompone() {
        return $this->idProductoCompone;
    }

    function getEstado() {
        return $this->estado;
    }

    function getIdUsuarioCreacion() {
        return $this->idUsuarioCreacion;
    }

    function getIdUsuarioModificacion() {
        return $this->idUsuarioModificacion;
    }

    function getFechaCreacion() {
        return $this->fechaCreacion;
    }

    function getFechaModificacion() {
        return $this->fechaModificacion;
    }

    function setIdProductoComposicion($idProductoComposicion) {
        $this->idProductoComposicion = $idProductoComposicion;
    }

    function setIdProductoCompuesto($idProductoCompuesto) {
        $this->idProductoCompuesto = $idProductoCompuesto;
    }

    function setCantidad($cantidad) {
        $this->cantidad = $cantidad;
    }

    function setIdProductoCompone($idProductoCompone) {
        $this->idProductoCompone = $idProductoCompone;
    }

    function setEstado($estado) {
        $this->estado = $estado;
    }

    function setIdUsuarioCreacion($idUsuarioCreacion) {
        $this->idUsuarioCreacion = $idUsuarioCreacion;
    }

    function setIdUsuarioModificacion($idUsuarioModificacion) {
        $this->idUsuarioModificacion = $idUsuarioModificacion;
    }

    function setFechaCreacion($fechaCreacion) {
        $this->fechaCreacion = $fechaCreacion;
    }

    function setFechaModificacion($fechaModificacion) {
        $this->fechaModificacion = $fechaModificacion;
    }


}
