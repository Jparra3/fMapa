<?php
namespace entidad;
class TipoNaturaleza {
    private $idTipoNaturaleza;
    private $tipoNaturaleza;
    private $estado;
    private $idUsuarioCracion;
    private $idUsuarioModificacion;
    private $fechaCreacion;
    private $fechaModificacion;
    private $codigo;
    
    function getIdTipoNaturaleza() {
        return $this->idTipoNaturaleza;
    }

    function getTipoNaturaleza() {
        return $this->tipoNaturaleza;
    }

    function getEstado() {
        return $this->estado;
    }

    function getIdUsuarioCracion() {
        return $this->idUsuarioCracion;
    }

    function getIdUsuarioModificacion() {
        return $this->idUsuarioModificacion;
    }

    function getFechaCreacion() {
        return $this->fechaCreacion;
    }

    function getFechaModificacion() {
        return $this->fechaModificacion;
    }

    function getCodigo() {
        return $this->codigo;
    }

    function setIdTipoNaturaleza($idTipoNaturaleza) {
        $this->idTipoNaturaleza = $idTipoNaturaleza;
    }

    function setTipoNaturaleza($tipoNaturaleza) {
        $this->tipoNaturaleza = $tipoNaturaleza;
    }

    function setEstado($estado) {
        $this->estado = $estado;
    }

    function setIdUsuarioCracion($idUsuarioCracion) {
        $this->idUsuarioCracion = $idUsuarioCracion;
    }

    function setIdUsuarioModificacion($idUsuarioModificacion) {
        $this->idUsuarioModificacion = $idUsuarioModificacion;
    }

    function setFechaCreacion($fechaCreacion) {
        $this->fechaCreacion = $fechaCreacion;
    }

    function setFechaModificacion($fechaModificacion) {
        $this->fechaModificacion = $fechaModificacion;
    }

    function setCodigo($codigo) {
        $this->codigo = $codigo;
    }


}
