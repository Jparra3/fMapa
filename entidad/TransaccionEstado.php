<?php
namespace entidad;
class TransaccionEstado {
    private $idTransaccionEstado;
    private $transaccionEstado;
    private $estado;
    private $inicial;
    
    function getInicial() {
        return $this->inicial;
    }

    function setInicial($inicial) {
        $this->inicial = $inicial;
    }
    
    public function getIdTransaccionEstado() {
        return $this->idTransaccionEstado;
    }

    public function getTransaccionEstado() {
        return $this->transaccionEstado;
    }

    public function getEstado() {
        return $this->estado;
    }

    public function setIdTransaccionEstado($idTransaccionEstado) {
        $this->idTransaccionEstado = $idTransaccionEstado;
    }

    public function setTransaccionEstado($transaccionEstado) {
        $this->transaccionEstado = $transaccionEstado;
    }

    public function setEstado($estado) {
        $this->estado = $estado;
    }


}
